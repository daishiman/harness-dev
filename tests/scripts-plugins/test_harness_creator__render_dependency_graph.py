def test_render_mermaid_basic_nodes_edges_and_styles():
    bundle = {
        "name": "demo",
        "capabilities": [
            {"kind": "skill", "ref": "skills/run-build-skill"},
            {"kind": "skill", "ref": "skills/ref-output-routing"},  # → ref に再分類
            {"kind": "agent", "ref": "agents/builder"},
        ],
        "dependencies": [
            {"from": "skills/run-build-skill", "to": "agents/builder", "type": "calls"},
        ],
    }
    out = MOD.render_mermaid(bundle)
    assert out.startswith("%% Generated by render-dependency-graph.py — bundle: demo")
    assert "graph LR" in out
    # subgraph がカテゴリごとに切られる。
    assert "subgraph skills[skill]" in out
    assert "subgraph refs[ref]" in out
    assert "subgraph agents[agent]" in out
    # エッジ。
    assert "n_skills_run_build_skill -- calls --> n_agents_builder" in out
    # classDef がカテゴリ分出る。
    for cat in MOD.STYLE:
        assert f"classDef {cat} " in out
    # class 割当(ノードが存在するカテゴリのみ)。
    assert "class n_agents_builder agent;" in out


def test_render_mermaid_completes_nodes_from_dependencies():
    # capabilities に無い ref を dependencies の from/to から補完し、prefix で kind 推定。
    bundle = {
        "name": "infer",
        "capabilities": [],
        "dependencies": [
            {"from": "skills/main", "to": "agents/sub", "type": "calls"},
            {"from": "skills/main", "to": "commands/cmd", "type": "calls"},
            {"from": "skills/main", "to": "hook:Stop/Guard", "type": "calls"},
            {"from": "skills/main", "to": "skills/ref-helper", "type": "reads"},
        ],
    }
    out = MOD.render_mermaid(bundle)
    # 各推定カテゴリが subgraph に現れる。
    assert "subgraph agents[agent]" in out
    assert "subgraph commands[command]" in out
    assert "subgraph hooks[hook]" in out
    assert "subgraph refs[ref]" in out  # skills/ref-* → ref
    assert "subgraph skills[skill]" in out  # skills/main → skill 既定
    # hook ノードラベルは hook: 形式のまま。
    assert '["hook:Stop/Guard"]' in out
    # type=reads エッジ。
    assert "-- reads --> n_skills_ref_helper" in out


def test_render_mermaid_default_edge_type_is_calls():
    bundle = {
        "name": "d",
        "capabilities": [{"kind": "skill", "ref": "skills/a"}],
        "dependencies": [{"from": "skills/a", "to": "skills/b"}],  # type 欠落
    }
    out = MOD.render_mermaid(bundle)
    assert "-- calls --> n_skills_b" in out


def test_render_mermaid_skips_edges_with_missing_endpoints():
    bundle = {
        "name": "d",
        "capabilities": [{"kind": "skill", "ref": "skills/a"}],
        "dependencies": [
            {"from": "skills/a", "to": ""},   # dst 欠落 → skip
            {"from": "", "to": "skills/a"},   # src 欠落 → skip
        ],
    }
    out = MOD.render_mermaid(bundle)
    assert "-->" not in out  # エッジは 1 本も出ない。


def test_render_mermaid_skips_capabilities_with_blank_ref():
    bundle = {
        "name": "d",
        "capabilities": [
            {"kind": "skill", "ref": ""},
            {"kind": "skill", "ref": "   "},
            {"kind": "skill", "ref": "skills/keep"},
        ],
        "dependencies": [],
    }
    out = MOD.render_mermaid(bundle)
    assert "n_skills_keep" in out
    # 空 ref はノード化されない(skill subgraph に keep だけ)。
    assert out.count('["') == 1


def test_render_mermaid_empty_bundle_uses_unknown_name():
    out = MOD.render_mermaid({})
    assert "bundle: (unknown)" in out
    assert "graph LR" in out
    # ノードもエッジも無い。
    assert "subgraph" not in out
    assert "-->" not in out


def test_render_mermaid_none_lists_treated_as_empty():
    # capabilities/dependencies が None でも空扱い(`or []`)。
    out = MOD.render_mermaid({"name": "n", "capabilities": None, "dependencies": None})
    assert "subgraph" not in out


