def _bundle_yaml(tmp_path):
    p = tmp_path / "plugin-composition.yaml"
    p.write_text(
        "name: tmpbundle\n"
        "capabilities:\n"
        "  - { kind: skill, ref: skills/run-a }\n"
        "  - { kind: agent, ref: agents/helper }\n"
        "dependencies:\n"
        "  - { from: skills/run-a, to: agents/helper, type: calls }\n",
        encoding="utf-8",
    )
    return p


def _run(args, cwd):
    return subprocess.run(
        [sys.executable, str(SCRIPT), *args],
        capture_output=True, text=True, cwd=cwd,
    )


def test_cli_help_exit0(tmp_path):
    proc = _run(["--help"], cwd=str(tmp_path))
    assert proc.returncode == 0
    assert "--bundle" in proc.stdout and "--self-test" in proc.stdout


def test_cli_bundle_to_stdout(tmp_path):
    bundle = _bundle_yaml(tmp_path)
    proc = _run(["--bundle", str(bundle)], cwd=str(tmp_path))
    assert proc.returncode == 0, proc.stderr
    assert "graph LR" in proc.stdout
    assert "n_skills_run_a -- calls --> n_agents_helper" in proc.stdout
    assert "# Dependency Graph: tmpbundle" in proc.stdout


def test_cli_bundle_out_writes_file(tmp_path):
    bundle = _bundle_yaml(tmp_path)
    out_file = tmp_path / "nested" / "graph.md"
    proc = _run(["--bundle", str(bundle), "--out", str(out_file)], cwd=str(tmp_path))
    assert proc.returncode == 0, proc.stderr
    assert out_file.exists()  # 親ディレクトリも作成される。
    body = out_file.read_text(encoding="utf-8")
    assert "graph LR" in body and "```mermaid" in body
    assert f"[ok] wrote {out_file.resolve()}" in proc.stdout


def test_cli_raw_out_has_no_fence(tmp_path):
    bundle = _bundle_yaml(tmp_path)
    out_file = tmp_path / "graph.mmd"
    proc = _run(["--bundle", str(bundle), "--out", str(out_file), "--raw"],
                cwd=str(tmp_path))
    assert proc.returncode == 0, proc.stderr
    body = out_file.read_text(encoding="utf-8")
    assert "graph LR" in body
    assert "```mermaid" not in body


def test_cli_missing_args_exit2(tmp_path):
    proc = _run([], cwd=str(tmp_path))
    assert proc.returncode == 2
    assert "--bundle" in proc.stderr or "usage" in proc.stderr.lower()


def test_cli_bundle_not_found_exit1(tmp_path):
    proc = _run(["--bundle", str(tmp_path / "missing.yaml")], cwd=str(tmp_path))
    assert proc.returncode == 1
    assert "bundle not found" in proc.stderr


def test_cli_self_test_runs(tmp_path):
    proc = _run(["--self-test"], cwd=str(tmp_path))
    assert proc.returncode == 0, proc.stderr
    assert "# Dependency Graph: harness-creator" in proc.stdout
