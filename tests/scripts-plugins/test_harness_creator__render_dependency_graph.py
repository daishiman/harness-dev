"""render-dependency-graph.py の純関数 + main CLI 契約を network 無しで網羅する。

render-dependency-graph.py は CapabilityBundle(plugin-composition.yaml)を読み込み、
capabilities[] / dependencies[] を Mermaid `graph LR` に変換する純レンダラであり、
実通信は一切しない。よって本テストは:

  - classify: skill+ref-* 再分類 / 非 skill はそのまま
  - node_id: 安全化 / 空入力フォールバック
  - node_label: hook: 形式そのまま / パス末尾セグメント
  - render_mermaid: capabilities ノード集合 / dependencies からの補完
    (hook:/agents//commands//ref-/既定 skill)/ subgraph / edge(type 既定 calls)/
    src・dst 欠落 skip / classDef・class 割当 / 空 bundle / name 既定
  - wrap_markdown: フェンス付き Markdown
  - _self_test_path: parents[3]/plugin-composition.yaml を指す
  - _load_yaml: PyYAML 経路(インストール済)
  - main: --self-test / --bundle / どちらも無し(exit2)/ 存在しない bundle(exit1)/
    --out 書き出し / --raw / stdout 出力

を tmp_path 上の実 YAML で genuine に assert する(repo 非汚染)。
"""
import importlib.util
import subprocess
import sys
from pathlib import Path

import pytest

ROOT = Path(__file__).resolve().parents[2]
SCRIPT = (
    ROOT
    / "plugins"
    / "harness-creator"
    / "skills"
    / "run-build-skill"
    / "scripts"
    / "render-dependency-graph.py"
)
SELF_TEST_BUNDLE = ROOT / "plugins" / "harness-creator" / "plugin-composition.yaml"

_SPEC = importlib.util.spec_from_file_location("render_dep_graph_under_test", SCRIPT)
MOD = importlib.util.module_from_spec(_SPEC)
_SPEC.loader.exec_module(MOD)


# --------------------------------------------------------------------------
# classify
# --------------------------------------------------------------------------

def test_classify_skill_ref_prefix_reclassified_to_ref():
    assert MOD.classify("skill", "skills/ref-output-routing") == "ref"


def test_classify_plain_skill_stays_skill():
    assert MOD.classify("skill", "skills/run-build-skill") == "skill"


def test_classify_non_skill_kinds_passthrough():
    assert MOD.classify("agent", "agents/foo") == "agent"
    assert MOD.classify("hook", "hook:Stop/Guard") == "hook"
    # kind=skill でない限り ref- prefix でも再分類しない。
    assert MOD.classify("command", "commands/ref-thing") == "command"


