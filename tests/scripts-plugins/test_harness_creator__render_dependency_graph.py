def test_main_requires_bundle_or_self_test(capsys):
    # どちらの引数も無いと argparse.error → SystemExit(2)。
    with pytest.raises(SystemExit) as ei:
        MOD.main([])
    assert ei.value.code == 2
    err = capsys.readouterr().err
    assert "--bundle" in err and "--self-test" in err


def test_main_bundle_not_found_returns_1(tmp_path, capsys):
    rc = MOD.main(["--bundle", str(tmp_path / "nope.yaml")])
    assert rc == 1
    assert "bundle not found" in capsys.readouterr().err


def test_main_self_test_renders_to_stdout(capsys):
    rc = MOD.main(["--self-test"])
    assert rc == 0
    out = capsys.readouterr().out
    assert "graph LR" in out
    assert "```mermaid" in out  # markdown 既定
    # self-test bundle 名がタイトルに反映。
    assert "# Dependency Graph: harness-creator" in out


def test_main_raw_flag_omits_markdown_fence(capsys):
    rc = MOD.main(["--self-test", "--raw"])
    assert rc == 0
    out = capsys.readouterr().out
    assert "graph LR" in out
    assert "```mermaid" not in out  # raw はフェンス無し
    assert not out.startswith("# Dependency Graph")


def test_main_inprocess_out_writes_file_and_makes_parent(tmp_path, capsys):
    # --out 経路(親 mkdir + write + [ok] 出力)を in-process で計測対象に乗せる。
    bundle = _bundle_yaml(tmp_path)
    out_file = tmp_path / "deep" / "nested" / "graph.md"
    rc = MOD.main(["--bundle", str(bundle), "--out", str(out_file)])
    assert rc == 0
    assert out_file.exists()
    body = out_file.read_text(encoding="utf-8")
    assert "graph LR" in body and "```mermaid" in body
    assert "[ok] wrote" in capsys.readouterr().out


def test_main_inprocess_bundle_path_resolved(tmp_path, capsys):
    # --bundle 指定経路(self-test でない側の bundle_path 解決)を踏む。
    bundle = _bundle_yaml(tmp_path)
    rc = MOD.main(["--bundle", str(bundle)])
    assert rc == 0
    out = capsys.readouterr().out
    assert "# Dependency Graph: tmpbundle" in out
    assert "n_skills_run_a -- calls --> n_agents_helper" in out


